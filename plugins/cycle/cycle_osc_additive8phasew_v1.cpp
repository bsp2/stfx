         voice->tmp6/*vsync*/ = (voice->tmp6/*vsync*/ * 15.0f) + 1.0f;
         voice->tmp5/*sin_tmp*/ = ((voice->tmp2_sin_phase + voice->tmp6/*phase*/));
         voice->tmp5/*sin_tmp*/ = ffrac_s(voice->tmp5/*sin_tmp*/);
