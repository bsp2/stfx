         voice->tmp11/*lut_a*/ = voice->eff_array_p_phase[( (unsigned int)voice->tmp12_lut_idx_a      ) & 7];
         voice->tmp14/*lut_b*/ = voice->eff_array_p_phase[(((unsigned int)voice->tmp12_lut_idx_a) + 1u) & 7];
