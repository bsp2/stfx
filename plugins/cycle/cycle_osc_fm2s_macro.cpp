         voice->tmp40_seq = (voice->tmp44_tri_tmp < 0.5f) ? (-1.0 + voice->tmp44_tri_tmp * 4.0f) : (1.0 - (voice->tmp44_tri_tmp - 0.5f)*4);
         voice->tmp41_tri_phase = ffrac_s(voice->tmp41_tri_phase + voice->tmp42_tri_speed);
         
