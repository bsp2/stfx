   (void)_note;
   (void)_vol;
   (void)_pan;

#ifdef OVERSAMPLE_FACTOR
   float noteSpeed = _freqHz / (voice->sample_rate * OVERSAMPLE_FACTOR);
#else
   float noteSpeed = _freqHz / voice->sample_rate;
#endif // OVERSAMPLE_FACTOR

   float modmag          = shared->params[PARAM_MAG         ]                       + voice->mods[MOD_MAG          ];
   float modcenter       = shared->params[PARAM_CENTER      ]                       + voice->mods[MOD_CENTER       ];
   float moddampen       = shared->params[PARAM_DAMPEN      ]                       + voice->mods[MOD_DAMPEN       ];
   float modamp          = shared->params[PARAM_AMP         ]                       + voice->mods[MOD_AMP          ];
   float moddetune       = shared->params[PARAM_DETUNE      ]                       + voice->mods[MOD_DETUNE       ];
